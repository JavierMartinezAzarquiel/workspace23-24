package bucleFor;

public class TablaDel7 {

	public static void main(String[] args) {
		/*
		 * Imprimir la tabla del 7
		 * 
		 *     7 x 0 = 0
		 *     7 x 1 = 7
		 *     7 x 2 = 14
		 *     ....
		 *     7 x 10 = 70
		 */
		
		System.out.println("Tabla del 7");
		for (int i = 0; i <= 10; i++) {
			System.out.println("7 x " + i + " = " + 7*i);
		}
		
		
	}

}
